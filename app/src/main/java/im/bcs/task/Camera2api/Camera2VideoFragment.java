    @Override
    public void onClick(View view) {
        switch (view.getId()) {
            case R.id.video:
                if (mIsRecordingVideo) {
                    stopRecordingVideo();
                    Intent intent = new Intent();
                    intent.putExtra("path",mNextVideoAbsolutePath);
                    getActivity().setResult(Activity.RESULT_OK,intent);
                    mNextVideoAbsolutePath = null;
                    getActivity().finish();
                } else {
                    startRecordingVideo();
                }
                break;
            case R.id.info: {
                if (ActivityCompat.checkSelfPermission(getActivity(),Manifest.permission.READ_CONTACTS)
                        != PackageManager.PERMISSION_GRANTED){
                    requestWriteReadPermissions();
                }
                takePicture();
                Intent intent = new Intent();
                intent.putExtra("path",mNextPicturePath);
                getActivity().setResult(Activity.RESULT_OK,intent);
                mNextPicturePath = null;
                getActivity().finish();
                }
                break;
            case R.id.cameraChange:
                if (type == 1){
                    type = 0;
                    closeCamera();
                    if (mTextureView.isAvailable()) {
                        openCamera(mTextureView.getWidth(), mTextureView.getHeight(),type);
                    } else {
                        mTextureView.setSurfaceTextureListener(mSurfaceTextureListener);
                    }
                }else {
                    type = 1;
                    closeCamera();
                    if (mTextureView.isAvailable()) {
                        openCamera(mTextureView.getWidth(), mTextureView.getHeight(),type);
                    } else {
                        mTextureView.setSurfaceTextureListener(mSurfaceTextureListener);
                    }
                }
                break;

            }
        }


    /**
     * Starts a background thread and its {@link Handler}.
     */
    private void startBackgroundThread() {
        mBackgroundThread = new HandlerThread("CameraBackground");
        mBackgroundThread.start();
        mBackgroundHandler = new Handler(mBackgroundThread.getLooper());
    }

    /**
     * Stops the background thread and its {@link Handler}.
     */
    private void stopBackgroundThread() {
        mBackgroundThread.quitSafely();
        try {
            mBackgroundThread.join();
            mBackgroundThread = null;
            mBackgroundHandler = null;
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
    }

